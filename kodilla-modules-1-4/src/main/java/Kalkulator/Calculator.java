package Kalkulator;

public class Calculator {

    public static void add(int a,int b)
    {
        System.out.println(a+b);
    }
    public static void substract(int a,int b)
    {
        System.out.println(a-b);
    }
    public static void main(String args[])
    {
        Calculator.add(5,2);
        Calculator.substract(5,2);
    }

}
